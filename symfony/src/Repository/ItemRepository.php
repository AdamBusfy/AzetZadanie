    /**
     * @return Item[]
     */
    public function findAllGreaterThanPrice(int $id): array
    {
        $entityManager = $this->getEntityManager();

        $query = $entityManager->createQuery(
            'SELECT p
            FROM App\Entity\Item p
            WHERE p.id > :item
            ORDER BY p.id ASC'
        )->setParameter('id', $id);

        // returns an array of Items objects
        return $query->getResult();
